create table ThuongHieu(
	MaTH int primary key,
	TenThuongHieu nvarchar (20)
);

CREATE TABLE SanPham
( 
    MaSanPham INT,
	TenDanhMuc NVARCHAR(255),
    MaThuongHieu INT NOT NULL,
    SoLuongTon INT,
    TenSanPham NVARCHAR(50),
    NhaCungCap NVARCHAR(255),
    Gia INT,
    XuatXu NVARCHAR(30),
    HinhAnh NVARCHAR(500),
    PRIMARY KEY (MaSanPham),
    FOREIGN KEY (MaThuongHieu) REFERENCES ThuongHieu(MaTH) ON DELETE CASCADE ON UPDATE CASCADE 
);


-- Tạo bảng tài khoản người dùng
CREATE TABLE TaiKhoan (
    MaTaiKhoan INT PRIMARY KEY,
    TenTaiKhoan NVARCHAR(255) NOT NULL,
    MatKhau NVARCHAR(255) NOT NULL,
    HoTen NVARCHAR(255),
    Email NVARCHAR(255),
    SoDienThoai NVARCHAR(20),
    DiaChi NVARCHAR(255),
    LoaiTaiKhoan NVARCHAR(20) CHECK (LoaiTaiKhoan IN ('nguoi_mua', 'nha_cung_cap'))
);

-- Tạo bảng đơn hàng
CREATE TABLE DonHang (
    MaDonHang INT PRIMARY KEY,
	MaTaiKhoan INT,
    NgayDatHang DATE,
    NgayGiaoHang DATE,
    TinhTrangDonHang NVARCHAR(50) CHECK (TinhTrangDonHang IN ('Dang xu ly', 'Dang giao', 'Da giao thanh cong', 'Huy')),
    MaNhaCungCap INT,
	FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan(MaTaiKhoan) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (MaNhaCungCap) REFERENCES TaiKhoan(MaTaiKhoan) 
	
);

-- Tạo bảng sản phẩm trong đơn hàng
CREATE TABLE SanPhamTrongDonHang (
    MaDonHang INT,
    MaSanPham INT,
    SoLuong INT,
    GiamGia FLOAT,
	PRIMARY KEY (MaDonHang,MaSanPham),
	FOREIGN KEY (MaDonHang) REFERENCES DonHang(MaDonHang) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (MaSanPham) REFERENCES SanPham(MaSanPham) ON DELETE CASCADE ON UPDATE CASCADE
);

-- Tạo bảng đánh giá sản phẩm
CREATE TABLE DanhGiaSanPham (
    MaDanhGia INT PRIMARY KEY,
	MaTaiKhoan INT,
    MaSanPham INT , 
    DiemDanhGia FLOAT, 
	FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan(MaTaiKhoan) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (MaSanPham) REFERENCES SanPham(MaSanPham) ON DELETE CASCADE ON UPDATE CASCADE
     
);

-- Tạo bảng bình luận sản phẩm
CREATE TABLE BinhLuanSanPham (
    MaBinhLuan INT PRIMARY KEY,
    MaTaiKhoan INT,
    MaSanPham INT,
	NoiDung NVARCHAR(MAX),
	FOREIGN KEY (MaTaiKhoan) REFERENCES TaiKhoan(MaTaiKhoan) ON DELETE CASCADE ON UPDATE CASCADE,
	FOREIGN KEY (MaSanPham) REFERENCES SanPham(MaSanPham) ON DELETE CASCADE ON UPDATE CASCADE
   
);
